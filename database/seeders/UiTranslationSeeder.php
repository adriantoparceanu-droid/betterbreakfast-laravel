    public function run(): void
    {
        $en = $this->enDict();
        $ro = $this->roDict();

        $flat = [];
        foreach ($this->flatten($en) as $key => $value) {
            $flat[] = ['locale' => 'en', 'key' => $key, 'value' => $value];
        }
        foreach ($this->flatten($ro) as $key => $value) {
            $flat[] = ['locale' => 'ro', 'key' => $key, 'value' => $value];
        }

        // insertOrIgnore: existing admin edits are never overwritten
        foreach ($flat as $row) {
            UiTranslation::firstOrCreate(
                ['locale' => $row['locale'], 'key' => $row['key']],
                ['value' => $row['value']]
            );
        }
    }

    private function flatten(array $array, string $prefix = ''): array
    {
        $result = [];
        foreach ($array as $key => $value) {
            $newKey = $prefix !== '' ? "{$prefix}.{$key}" : (string) $key;
            if (is_array($value)) {
                $result = array_merge($result, $this->flatten($value, $newKey));
            } else {
                $result[$newKey] = (string) $value;
            }
        }
        return $result;
    }

